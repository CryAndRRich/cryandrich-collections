DROP TRIGGER IF EXISTS CHANTUONGTAC;
DELIMITER //
CREATE TRIGGER CHANTUONGTAC
BEFORE INSERT ON TUONGTAC_HOATCHAT
FOR EACH ROW
BEGIN
    IF NEW.MAHC1 = NEW.MAHC2 THEN
        SIGNAL SQLSTATE '45000'
            SET MESSAGE_TEXT = 'Không thêm bản ghi có hai hoá chất trùng nhau';
    END IF;
END //
DELIMITER;
