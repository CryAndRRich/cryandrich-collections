DROP DATABASE IF EXISTS QLThuoc;
CREATE DATABASE QLThuoc;
USE QLThuoc;

-- A.1
CREATE TABLE THUOC (
    MATHUOC VARCHAR(3) PRIMARY KEY,
    TENTHUOC VARCHAR(30)
);

CREATE TABLE HOATCHAT (
    MAHC VARCHAR(4) PRIMARY KEY,
    TENHC VARCHAR(30)
);

CREATE TABLE THANHPHAN_THUOC (
    MATHUOC VARCHAR(3),
    MAHC VARCHAR(4),
    PRIMARY KEY (MATHUOC, MAHC),
    FOREIGN KEY (MATHUOC) REFERENCES THUOC(MATHUOC),
    FOREIGN KEY (MAHC) REFERENCES HOATCHAT(MAHC)
);

CREATE TABLE TUONGTAC_HOATCHAT(
    MAHC1 VARCHAR(4),
    MAHC2 VARCHAR(4),
    MUCDO VARCHAR(30),
    PRIMARY KEY (MAHC1, MAHC2),
    FOREIGN KEY (MAHC1) REFERENCES HOATCHAT(MAHC), 
    FOREIGN KEY (MAHC2) REFERENCES HOATCHAT(MAHC)
); 

