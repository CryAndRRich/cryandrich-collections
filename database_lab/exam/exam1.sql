-- B.1
SELECT t.MATHUOC, t.TENTHUOC
FROM THUOC t
JOIN THANHPHAN_THUOC tp ON t.MATHUOC = tp.MATHUOC
JOIN HOATCHAT h ON tp.MAHC = h.MAHC
WHERE h.TENHC = 'Paracetamol';

-- B.2
SELECT h.MAHC, h.TENHC, COUNT(*) AS SOTHUOC
FROM HOATCHAT h
JOIN THANHPHAN_THUOC tp ON tp.MAHC = h.MAHC
GROUP BY h.MAHC
HAVING SOTHUOC >= ALL(
    SELECT COUNT(*)
    FROM HOATCHAT h2
    JOIN THANHPHAN_THUOC tp2 ON tp2.MAHC = h2.MAHC
    GROUP BY h2.MAHC
);


-- B.3
SELECT h.MAHC, h.TENHC
FROM HOATCHAT h
WHERE h.MAHC NOT IN (
    SELECT DISTINCT MAHC1
    FROM TUONGTAC_HOATCHAT
)
AND h.MAHC NOT IN (
    SELECT DISTINCT MAHC2
    FROM TUONGTAC_HOATCHAT
);

-- B.4
DELETE FROM THUOC
WHERE MATHUOC NOT IN (
    SELECT DISTINCT MATHUOC
    FROM THANHPHAN_THUOC
);

-- B.5
SELECT t1.MATHUOC, t1.TENTHUOC, t2.MATHUOC, t2.TENTHUOC
FROM THANHPHAN_THUOC tp1
CROSS JOIN THANHPHAN_THUOC tp2
JOIN TUONGTAC_HOATCHAT tt ON tp1.MAHC = tt.MAHC1 AND tp2.MAHC = tt.MAHC2
JOIN THUOC t1 ON t1.MATHUOC = tp1.MATHUOC
JOIN THUOC t2 ON t2.MATHUOC = tp2.MATHUOC
WHERE tt.MUCDO = 'Nghiêm trọng';

-- B.6
SELECT MATHUOC, TENTHUOC
FROM THUOC
WHERE MATHUOC NOT IN (
    SELECT MATHUOC 
    FROM THANHPHAN_THUOC 
    WHERE MAHC IN (
        SELECT MAHC1 FROM TUONGTAC_HOATCHAT
        UNION
        SELECT MAHC2 FROM TUONGTAC_HOATCHAT
    )
);

-- B.7
SELECT DISTINCT t1.MATHUOC, t1.TENTHUOC, tp1.MAHC, tp2.MAHC, tt.MUCDO
FROM THANHPHAN_THUOC tp1
CROSS JOIN THANHPHAN_THUOC tp2
JOIN TUONGTAC_HOATCHAT tt ON tp1.MAHC = tt.MAHC1 AND tp2.MAHC = tt.MAHC2
JOIN THUOC t1 ON t1.MATHUOC = tp1.MATHUOC
JOIN THUOC t2 ON t2.MATHUOC = tp2.MATHUOC;

