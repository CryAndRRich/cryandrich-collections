-- A.1
CREATE TABLE VEXE (
    MATHE VARCHAR (4) PRIMARY KEY,
    TRANGTHAI VARCHAR (30)
);

CREATE TABLE XE (
    BIENSO VARCHAR (10) PRIMARY KEY,
    LOAIXE VARCHAR (30)
);

CREATE TABLE VETHANG (
    MATHE VARCHAR (4) PRIMARY KEY,
    BIENSO VARCHAR (30),
    TENCHUXE VARCHAR (30),
    NGAYHETHAN DATETIME,
    FOREIGN KEY (MATHE) REFERENCES VEXE(MATHE)
);

CREATE TABLE LUOTGUI (
    MALG VARCHAR (5) PRIMARY KEY,
    MATHE VARCHAR (4),
    BIENSOQUETDUOC VARCHAR (10),
    THOIGIANVAO DATETIME,
    THOIGIANRA DATETIME DEFAULT NULL,
    FOREIGN KEY (MATHE) REFERENCES VEXE(MATHE),
    FOREIGN KEY (BIENSOQUETDUOC) REFERENCES XE(BIENSO)
);

