DELIMITER //
CREATE TRIGGER KiemTraXeTrongBai
BEFORE INSERT ON LUOTGUI
FOR EACH ROW
BEGIN
    IF EXISTS (
        SELECT 1 FROM LUOTGUI
        WHERE BIENSOQUETDUOC = NEW.BIENSOQUETDUOC
        AND THOIGIANRA IS NULL
    ) THEN
        SIGNAL SQLSTATE '45000'
        SET MESSAGE_TEXT = 'Xe vẫn còn trong bãi';
    END IF;
END //
DELIMITER ;
