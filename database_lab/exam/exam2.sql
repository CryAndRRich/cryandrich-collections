-- B.2
SELECT LG.MALG, LG.MATHE, LG.BIENSOQUETDUOC, LG.THOIGIANVAO, LG.THOIGIANRA
FROM LUOTGUI LG
JOIN VETHANG VT ON LG.MATHE = VT.MATHE
WHERE LG.BIENSOQUETDUOC != VT.BIENSO;

-- B.3
SELECT *
FROM VETHANG
WHERE DATEDIFF(NGAYHETHAN, CURDATE()) BETWEEN 0 AND 2;

-- B.4
SELECT HOUR(THOIGIANVAO) AS GIOCAODIEM, COUNT(*) AS LUOTXE
FROM LUOTGUI
WHERE MONTH(THOIGIANVAO) = MONTH(CURDATE())
AND YEAR(THOIGIANVAO) = YEAR(CURDATE())
GROUP BY HOUR(THOIGIANVAO)
ORDER BY COUNT(*) DESC
LIMIT 1;

-- B.5
SELECT CURDATE() AS TODAY, COUNT(*) AS LUOTGUIXEBANGVE
FROM LUOTGUI
WHERE DATE(THOIGIANVAO) = CURDATE()
AND MATHE NOT IN (
    SELECT MATHE 
    FROM VETHANG
);

-- B.6
SELECT MATHE, COUNT(DISTINCT BIENSOQUETDUOC) AS SOXESUDUNG
FROM LUOTGUI
GROUP BY MATHE
HAVING COUNT(DISTINCT BIENSOQUETDUOC) >= 2;

-- B.7
SELECT X.LOAIXE, AVG(TIMESTAMPDIFF(MINUTE, LG.THOIGIANVAO, LG.THOIGIANRA)) / 60 AS TGGUITRUNGBINH
FROM LUOTGUI LG
JOIN XE X ON LG.BIENSOQUETDUOC = X.BIENSO
WHERE LG.THOIGIANRA IS NOT NULL
GROUP BY X.LOAIXE
HAVING AVG(TIMESTAMPDIFF(MINUTE, LG.THOIGIANVAO, LG.THOIGIANRA)) / 60 > 6;

